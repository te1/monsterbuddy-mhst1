<template>
  <div
    v-show="show || force"
    class="note flex gap-3 bg-[#fef9c3] dark:bg-[#eab308]/25 px-4 py-3"
  >
    <div class="flex-1">
      <a
        class="link"
        href="https://www.monsterhunter.com/stories3"
        target="_blank"
      >
        Monster Hunter Stories 3: Twisted Reflection</a
      >
      has been released! Expect updates to this site soon™. For now just enjoy
      the game!
    </div>

    <div v-if="!force" class="-mt-3 -mr-3">
      <AppIconButton
        :icon="['fas', 'times']"
        title="Don't show this anymore"
        class="text-xl"
        @click="hide"
      />
    </div>
  </div>
</template>

<script>
export default {
  name: 'Mhst2Banner',

  props: {
    force: {
      type: Boolean,
      required: false,
      default: false,
    },
  },

  computed: {
    history() {
      return this.$useHistoryStore();
    },

    show() {
      return this.history.showMhst2Banner;
    },
  },

  methods: {
    hide() {
      this.history.hideMhst2Banner();
    },
  },
};
</script>
